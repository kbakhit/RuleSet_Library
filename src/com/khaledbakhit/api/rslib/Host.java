package com.khaledbakhit.api.rslib;
/**
 * Host class defines the name of program implementing the RuleSet Library.
 * This is used to create directories defined by the default implementation.
 * 
 * @author Khaled Bakhit
 * @since 3.0
 * @version 15/09/2011
 */
public class Host 
{
    /**
     * Name of the host application using RuleSet library. 
     */
    public static String HostProgramName;
    /**
     * Version of the host application using RuleSet library.      
     */
    public static float HostProgramVersion;
    /**
     * Name of the host application's author.
     */
    public static String Author;
    /**
     * Email address of host application's author. 
     */
    public static String Email;
}